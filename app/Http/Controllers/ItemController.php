     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create($id, $itemid)
    {
        $user = User::find($id);
        $item = Item::find($itemid);
        Transaction::create([
            'user_id' => $user->id,
            'item_id' => $item->id,
            'transaction_date' => Carbon::now()
        ]);
        return redirect('/user/'.$user->id.'/transaction');
    }

